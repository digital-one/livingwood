<h4>Solution for low energy constructions</h4>

<p>Concept System&reg; 104 is our latest and most innovative inward opening window system, designed to increase comfort and decrease energy consumption to a minimum. The extreme insulation levels (Uf down to 0.88 W/m²K) are achieved by a patented technology, in which insulation strips with increased stability are combined with special foam in a way that no extra manipulation is needed during the production of the window elements. This guarantees a constant quality of the assembled element, making this window perfectly suited to passive constructions.</p>

<p>Besides a high insulation level, air tightness is the most important factor determining a window’s energy efficiency. Therefore, a new set of specific gaskets was developed to ensure the ultimate water and air tightness of the system, making this system applicable for extreme conditions such as coastal areas and high altitudes. The increased built-in depth (104mm) contributes to the strength and stability of the elements, allowing large dimensions, even with the application of triple glazing. The window system perfectly matches the CS 104 door system. Additionally, the system offers several levels of burglar resistance (class 2 and 3).
</p>
